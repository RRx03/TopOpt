std::vector<double> MarchingCubes::elementToNodal(
    const std::vector<double>& elemField, const Grid3D& grid) {
    const int nN = grid.nNodes();
    std::vector<double> nodal(static_cast<size_t>(nN), 0.0);
    std::vector<int> count(static_cast<size_t>(nN), 0);
    for (int ez = 0; ez < grid.nelz(); ++ez)
        for (int ey = 0; ey < grid.nely(); ++ey)
            for (int ex = 0; ex < grid.nelx(); ++ex) {
                const double v =
                    elemField[static_cast<size_t>(grid.elemId(ex, ey, ez))];
                for (int nid : grid.elementNodes(ex, ey, ez)) {
                    nodal[static_cast<size_t>(nid)] += v;
                    count[static_cast<size_t>(nid)] += 1;
                }
            }
    for (int n = 0; n < nN; ++n)
        if (count[static_cast<size_t>(n)] > 0)
            nodal[static_cast<size_t>(n)] /=
                count[static_cast<size_t>(n)];
    return nodal;
}

std::vector<MCTriangle> MarchingCubes::extract(
    const std::vector<double>& nodalField, const Grid3D& grid, double iso,
    double spacing, const Eigen::Vector3d& origin) {
    std::vector<MCTriangle> out;

    for (int ez = 0; ez < grid.nelz(); ++ez)
        for (int ey = 0; ey < grid.nely(); ++ey)
            for (int ex = 0; ex < grid.nelx(); ++ex) {
                // Gather 8 corner values/positions in Bourke order.
                double val[8];
                Eigen::Vector3d pos[8];
                for (int c = 0; c < 8; ++c) {
                    const int i = ex + kCorner[c][0];
                    const int j = ey + kCorner[c][1];
                    const int k = ez + kCorner[c][2];
                    val[c] = nodalField[static_cast<size_t>(grid.nodeId(i, j, k))];
                    pos[c] = origin + spacing * Eigen::Vector3d(i, j, k);
                }

                int cubeIndex = 0;
                for (int c = 0; c < 8; ++c)
                    if (val[c] < iso) cubeIndex |= (1 << c);

                const int edges = edgeTable[cubeIndex];
                if (edges == 0) continue;

                // Field gradient at the cell (trilinear form gradient at centre),
                // in Grid3D local corner order. -grad points toward the void.
                auto f = [&](int di, int dj, int dk) {
                    const int i = ex + di, j = ey + dj, k = ez + dk;
                    return nodalField[static_cast<size_t>(grid.nodeId(i, j, k))];
                };
                Eigen::Vector3d grad;
                grad.x() = 0.25 * ((f(1, 0, 0) - f(0, 0, 0)) + (f(1, 1, 0) - f(0, 1, 0)) +
                                   (f(1, 0, 1) - f(0, 0, 1)) + (f(1, 1, 1) - f(0, 1, 1)));
                grad.y() = 0.25 * ((f(0, 1, 0) - f(0, 0, 0)) + (f(1, 1, 0) - f(1, 0, 0)) +
                                   (f(0, 1, 1) - f(0, 0, 1)) + (f(1, 1, 1) - f(1, 0, 1)));
                grad.z() = 0.25 * ((f(0, 0, 1) - f(0, 0, 0)) + (f(1, 0, 1) - f(1, 0, 0)) +
                                   (f(0, 1, 1) - f(0, 1, 0)) + (f(1, 1, 1) - f(1, 1, 0)));
                const Eigen::Vector3d outward = -grad;  // toward decreasing field

                // Interpolate the surface vertex on each cut edge.
                Eigen::Vector3d vert[12];
                for (int e = 0; e < 12; ++e)
                    if (edges & (1 << e)) {
                        const int a = kEdge[e][0], b = kEdge[e][1];
                        vert[e] = vertexInterp(iso, pos[a], pos[b], val[a], val[b]);
                    }

                for (int t = 0; triTable[cubeIndex][t] != -1; t += 3) {
                    MCTriangle tri;
                    tri.v[0] = vert[triTable[cubeIndex][t + 0]];
                    tri.v[1] = vert[triTable[cubeIndex][t + 1]];
                    tri.v[2] = vert[triTable[cubeIndex][t + 2]];

                    Eigen::Vector3d gn =
                        (tri.v[1] - tri.v[0]).cross(tri.v[2] - tri.v[0]);
                    // Orient winding so the normal points toward the void.
                    if (gn.dot(outward) < 0.0) {
                        std::swap(tri.v[1], tri.v[2]);
                        gn = -gn;
                    }
                    const double len = gn.norm();
                    tri.n = (len > 1e-300) ? (gn / len) : outward.normalized();
                    out.push_back(tri);
                }
            }
    return out;
}

bool MarchingCubes::writeSTL(const std::string& path,
                             const std::vector<MCTriangle>& tris) {
    std::ofstream fout(path, std::ios::binary);
    if (!fout) {
        std::fprintf(stderr, "MarchingCubes: cannot open %s\n", path.c_str());
        return false;
    }
    char header[80] = {0};
    fout.write(header, 80);
    const std::uint32_t ntri = static_cast<std::uint32_t>(tris.size());
    fout.write(reinterpret_cast<const char*>(&ntri), 4);
    for (const MCTriangle& t : tris) {
        float buf[12];
        buf[0] = static_cast<float>(t.n.x());
        buf[1] = static_cast<float>(t.n.y());
        buf[2] = static_cast<float>(t.n.z());
        for (int i = 0; i < 3; ++i) {
            buf[3 + 3 * i + 0] = static_cast<float>(t.v[i].x());
            buf[3 + 3 * i + 1] = static_cast<float>(t.v[i].y());
            buf[3 + 3 * i + 2] = static_cast<float>(t.v[i].z());
        }
        fout.write(reinterpret_cast<const char*>(buf), 48);
        const std::uint16_t attr = 0;
        fout.write(reinterpret_cast<const char*>(&attr), 2);
    }
    std::printf("STL: %u triangles -> %s\n", ntri, path.c_str());
    return true;
}

